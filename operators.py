# Arithmetic operators
x = 10
y = 5
print("Arithmetic Operators:")
print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x // y =", x // y)
print("x % y =", x % y)
print("x ** y =", x ** y)

# Comparison operators
print("\nComparison Operators:")
print("x > y is", x > y)
print("x < y is", x < y)
print("x == y is", x == y)
print("x != y is", x != y)
print("x >= y is", x >= y)
print("x <= y is", x <= y)

# Logical operators
a = True
b = False
print("\nLogical Operators:")
print("a and b is", a and b)
print("a or b is", a or b)
print("not a is", not a)

# Bitwise operators
m = 10  # 1010 in binary
n = 4   # 0100 in binary
print("\nBitwise Operators:")
print("m & n is", m & n)
print("m | n is", m | n)
print("m ^ n is", m ^ n)
print("~m is", ~m)
print("m << 2 is", m << 2)
print("m >> 2 is", m >> 2)

# Assignment operators
z = 15
print("\nAssignment Operators:")
z += 5
print("z += 5, z is", z)
z -= 5
print("z -= 5, z is", z)
z *= 2
print("z *= 2, z is", z)
z /= 2
print("z /= 2, z is", z)
z %= 5
print("z %= 5, z is", z)
z //= 2
print("z //= 2, z is", z)
z **= 3
print("z **= 3, z is", z)

# Identity operators
list1 = [1, 2, 3]
list2 = [1, 2, 3]
print("\nIdentity Operators:")
print("list1 is list2:", list1 is list2)
print("list1 is not list2:", list1 is not list2)

# Membership operators
print("\nMembership Operators:")
print("1 in list1:", 1 in list1)
print("4 not in list1:", 4 not in list1)